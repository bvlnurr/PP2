#Use the copy() method
thislist = ["apple", "banana", "cherry"]
mylist = thislist.copy()
print(mylist)

#Use the list() method
thislist1 = ["apple", "banana", "cherry"]
mylist = list(thislist1)
print(mylist)

#Use the slice Operator
thislist2 = ["apple", "banana", "cherry"]
mylist1 = thislist2[:]
print(mylist1)