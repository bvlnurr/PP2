#Remove Specified Item
thislist = ["apple", "banana", "cherry"]
thislist.remove("banana")
print(thislist)

#Remove Specified Index
thislist1 = ["apple", "banana", "cherry"]
thislist1.pop(1)
print(thislist1)

#keyword del
list1 = ["apple", "banana", "cherry"]
del list1[0]
print(list1)

#Clear the List
thislist2 = ["apple", "banana", "cherry"]
thislist2.clear()
print(thislist2)